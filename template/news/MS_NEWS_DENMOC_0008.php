                        <div class="panel-body">
                            <p>Most nationalities are required to obtain tourist visa in advance to travel to Vietnam.
                                When obtaining your visa you should allow 3 weeks for processing with your nearest
                                embassy or consulate, the cost is approximately US$60 to US$100. Please check with your
                                embassy or consulate for further requirements.</p>
                            <p>Some nationalities are exempt from visas for a stay of a maximum of 15 days, including
                                British, German, French, Spanish and Italian citizens travelling to Vietnam (for all
                                purposes). This exemption period is currently effective until 30 June 2021, and is based
                                upon meeting all conditions prescribed by Vietnamese laws. If you are planning on
                                staying in Vietnam for longer than 15 days you will need to obtain a visa extension in
                                advance (please contact your relevant Consulate or Embassy). Please also check
                                requirements if you plan to re-enter Vietnam.</p>
                            <p>An eVisa is available for some nationalities including passport holders from Australia
                                (from 2018), France, Ireland, Italy, Germany, Spain, the United Kingdom, Canada and the
                                United States of America, valid for a single entry of a maximum of 30 days stay in
                                Vietnam. For information on obtaining an eVisa visit the immigration website:
                                https://evisa.xuatnhapcanh.gov.vn/</p>
                            <p>Evisas take up to 3 days to be processed. You will need a clear electronic copy of your
                                passport data page and passport photo, and to make a non-refundable payment as outlined
                                on the immigration website.<br>
                                Keep the customs and immigration form you receive on arrival, as you need it to complete
                                exit formalities on departure.</p>
                            <p>If your visa application asks for a point of contact, please write: Intrepid Vietnam 5th
                                floor HiPT Building, 152 Thuy Khue Street, Thuy Khue, Tay Ho, Hanoi, Ph +84 4 3715 0996.
